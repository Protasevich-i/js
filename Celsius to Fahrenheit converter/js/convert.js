/* project. Цельсий в Фаренгейты.
Завершите функцию convertCelsiusToFahrenheit, чтобы она возвращала полученную температуру
в градусах Цельсия, в градусах Фаренгейта. Формула для перевода
следующая: (numberInCelsius * 9 / 5) + 32 */

export function convertCelsiusToFahrenheit(celsius) {
    return (celsius * 9 / 5) + 32
}